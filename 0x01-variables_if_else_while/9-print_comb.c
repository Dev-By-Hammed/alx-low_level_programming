#include <stdio.h>

/**
 * main - it  Print out numbers between 0 to 9 seperated  with commas
 *and seperated by spaces
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	int i;

	for (i = 48; i < 58; i++)
	{
		putchar(i);
		if (i != 57)
		{
			putchar(',');
			putchar(' ');
		}
	}
	putchar('\n');
	return (0);
}
